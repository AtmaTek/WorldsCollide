def byte(npc_bit):
    return npc_bit // 8

def bit(npc_bit):
    return npc_bit % 8

def address(npc_bit):
    return 0x1e80 + byte(npc_bit)

def event_byte(npc_bit):
    npc_bit_start = 0x1ee0
    event_bit_start = 0x1e80
    return (npc_bit // 8) - (npc_bit_start - event_bit_start)

def event_bit(npc_bit):
    return npc_bit % 8
